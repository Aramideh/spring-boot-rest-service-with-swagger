package com.example.restservice.services;

public class MathPair {

    double x;
    double y;

    public MathPair(){
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
